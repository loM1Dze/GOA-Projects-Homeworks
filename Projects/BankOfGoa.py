def show_transactions(transactions):        # ფუნქციის განსაზღვრა, იღებს ტრანზაქციების სიას (tuple-ები/წყვილები)
    print("ტრანზაქციების ისტორია:")        # სათაურის დაბეჭდვა
    if transactions:                        # თუ სია ცარიელი არაა (Truthy შემოწმება)
        for t in transactions:              # სიაში თითოეულ ჩანაწერზე იტერაცია
            print(f"{t[0]}: {t[1]} ₾")      # თითო ჩანაწერის პირველი ელემენტი (აღწერა/თარიღი) და მეორე (თანხა) ლარებში
    else:                                   # წინააღმდეგ შემთხვევაში, როცა სია ცარიელია
        print("ტრანზაქციები არ არის")       # შეტყობინება, რომ ტრანზაქციები არ მოიძებნა

while True:                                 # უსასრულო ციკლი მენიუსთვის (გავჩერდებით break-ით)
    print("\n--- transaction history ---")  # ვიზუალური გამყოფი/სათაური
    print("1. ტრანზაქციების ისტორია")       # მენიუს 1-ლი პუნქტის გამოჩენა
    print("2. გამოსვლა")                    # მენიუს 2-ე პუნქტის გამოჩენა
    choice = input("აირჩიეთ ოპერაცია: ")       # მომხმარებლის არჩევანის მიღება (სტრიქონი)

    if choice == "1":                        # თუ მომხმარებელმა შეიყვანა "1"
        show_transactions(transactions)      # გამოიძახე ფუნქცია და აჩვენე ტრანზაქციების სია
    elif choice == "2":                      # თუ მომხმარებელმა შეიყვანა "2"
        print("გამოსვლა...")                 # შეტყობინება გამოსვლაზე
        break                                # ციკლიდან გასვლა (პროგრამის დასრულება)
    else:                                    # ნებისმიერი სხვა შეყვანის შემთხვევაში
        print("არასწორი არჩევანი!")          # შეცდომის შეტყობინება

