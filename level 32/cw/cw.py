# for ლუპით დაუარეთ რიცხვებს 0 დან 100 ჩათვლით, თუ რიცხვი კენტია დაბეჭდეთ 'კენტია' თუ ლუწია დაბეჭდეთ 'ლუწია'.
# თუ რიცხვი ნულია დაბეჭდეთ 'ნული'


for i in range(101):        # 0..100 (ჩათვლით)
    if i == 0:
        print(i, "ნული")
    elif i % 2 == 0:
        print(i, "ლუწია")
    else:
        print(i, "კენტია")
